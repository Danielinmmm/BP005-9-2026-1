#include <iostream>
using namespace std;

int main() {

    int a, b;

    cout << "Ingrese dos numeros: ";
    cin >> a >> b;

    cout << "Suma: " << a + b << endl;
    cout << "Resta: " << a - b << endl;
    cout << "Multiplicacion: " << a * b << endl;
    cout << "Division: " << a / b << endl;
    cout << "Residuo: " << a % b << endl;

    return 0;
}
